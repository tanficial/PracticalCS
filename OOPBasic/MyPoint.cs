namespace OOPBasic;

public struct MyPointStruct {
    public int X { get; set; }
    public int Y { get; set; }

    // 생성자
    public MyPointStruct(int x, int y) {
        this.X = x;
        this.Y = y;
    }
}

public class MyPointClass {
    public int X { get; set; }
    public int Y { get; set; }

    // 생성자
    public MyPointClass(int x, int y) {
        this.X = x;
        this.Y = y;
    }
}